#include <unistd.h>

int	main(int ac, char **av)
{
	int i =1;
	int j;


	if (ac < 2)
	{
		write (1, "\n",1);
		return (0);
	}
	while (i < ac)
	{
		j=0;
		if(av[i][j] >= 'a' && av[i][j] <= 'z')
			av[i][j]-=32;
		write(1, &av[i][j],1);
		j++;
		while(av[i][j])
		{
			if((av[i][j-1]>='a' && av[i][j-1] <='z') || (av[i][j-1] >= 'A' && av[i][j-1] <= 'Z') || (av[i][j-1]>= '0' && av[i][j-1] <= '9') || av[i][j-1] == 39)
			{
				if(av[i][j] >= 'A' && av[i][j] <= 'Z')
					av[i][j]+=32;
			}
			else
			{
				if(av[i][j] >= 'a' && av[i][j] <= 'z')
					av[i][j]-=32;
			}
			write(1, &av[i][j],1);
			j++;
		}
		write(1, "\n",1);
		i++;
	}
	return (0);
}
