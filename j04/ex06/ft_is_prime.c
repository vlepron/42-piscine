int	ft_is_prime(int nb)
{
	int n;

	if (nb <= 1)
		return (0);
	else
	{
		n = nb - 1;
		while (n >= 2)
		{
			if (nb % n == 0)
				return (0);
			n--;
		}
	}
	return (1);
}
