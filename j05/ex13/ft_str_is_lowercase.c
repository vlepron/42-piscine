int	ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (!((str[i] < 123 && str[i] > 96) || str[i] == '\0'))
			return (0);
		i++;
	}
	return (1);
}
